<template>
  <div class="flex flex-col pl-14 pt-4">
    <div class="text-lg py-3" v-for="category in categories" :key="category.id">
      <router-link :to="{path: '/products', query: { category: category.name}}">
        {{ category.name }}
      </router-link>
    </div>
  </div>
</template>

<script>
import { mapGetters } from "vuex"

export default {
  name: "CategoryMenu",
  created() {
    this.loadCategories()
  },
  mounted() {
    this.latestRoute = this.$route.query.category
  },
  methods: {
    loadCategories() {
      this.$store.dispatch("catalogStore/loadCategories")
    },
  },
  computed: {
    ...mapGetters({
      categories: "catalogStore/getCategories",
    }),
  },
}
</script>

